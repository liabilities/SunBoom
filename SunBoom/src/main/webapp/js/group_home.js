function setAvatar() {
    var outer = document.createElement("div");
    var setAvatarBox = document.createElement("div");
    outer.id = "outer";
    setAvatarBox.id = "setAvatarBox";
    outer.appendChild(setAvatarBox);
    document.body.appendChild(outer);
}